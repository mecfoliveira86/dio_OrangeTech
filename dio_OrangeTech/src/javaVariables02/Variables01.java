package javaVariables02;

public class Variables01 {
	
	public static void main(String [] args) {
		//variável para número inteiro: usar int
		int numero = 9;
		
		//variável para número decimal: usar float
		float numeroF = 9.56f;
		
		//variável para número decimal: usar double
		double numeroD = 9.589456d;
		
		//variável para texto
		String textoS = "Exemplo de texto";
		
		//variável para booleanos (TRUE / FALSE)
		boolean status = true;	
		
		System.out.println("Número inteiro " + numero);
		System.out.println("Número inteiro " + numeroF);
		System.out.println("Número inteiro " + numeroD);
		System.out.println("Número inteiro " + textoS);
		System.out.println("Número inteiro " + status);
		
	}

}
